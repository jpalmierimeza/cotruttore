                <p>El principal objetivo de un <strong>estudio topográfico</strong> es determinar las características físicas del terreno, tales como sus dimensiones, elevaciones, pendientes, cuerpos de agua, vegetación y obstáculos naturales o artificiales. La información obtenida se utiliza para:</p>
                
                <p>
                - <strong>Elaborar</strong> planos topográficos precisos que sirvan de base para los diseños de ingeniería civil, arquitectónica y urbana. <br>
                - <strong>Identificar</strong> las condiciones geográficas y ambientales que puedan influir en la construcción o el desarrollo de proyectos. <br>
                - <strong>Determinar</strong> la viabilidad de un proyecto en función del terreno, evaluando riesgos como inundaciones, deslizamientos de tierra o inestabilidad geotécnica. <br>
                - <strong>Optimizar</strong> el uso del suelo y recursos naturales involucradas en la infraestructura. <br>
